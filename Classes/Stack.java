import java.util.Stack;

import java.util.Vector;

public class Stack<E> extends Vector<E> {
  
  // No-Constructor that creates an empty stack
  public Stack() {

  }


  // Method that returns true if the stack is empty, and false otherwise
  public boolean empty() {}

  // Method that return the top element in the stack
  public E peek() {}

  // Method that removes and returns the top element in the stack
  public E pop() {}

  // Method that adds a new element to the top of the stack
  public E push(E obj) {}

  // Method that returns the position of the specified element in the stack
  public int search(Object obj) {}
}
